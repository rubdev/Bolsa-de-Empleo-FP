    private boolean actulizarDatosCorreo() {
        boolean esActualizar=false;
        try {
            ResultSet rs;
            PreparedStatement consulta;
            consulta = this.bd.dameConexion().prepareStatement("SELECT * FROM infoemail WHERE id = ?");
            consulta.setInt(1, 1);
            rs = consulta.executeQuery();
            if (rs.next()) {
                esActualizar=true;
                System.out.println("MENSAJE: Ya hay datos de email, debes modificar");
            } else {
                System.out.println("MENSAJE: NO hay datos de email, debes guardar");
            }
        } catch (SQLException e) {
            System.out.println("ERROR: "+e.getMessage());
        }
        return esActualizar;
    }
    
