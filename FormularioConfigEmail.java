    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        System.out.println("MENSAJE: Voy a actualizar datos de email y contraseña");
        String correo, pass;
        boolean correcto=true;
        boolean modificar = actulizarDatosCorreo();
        correo = inputCorreo.getText();
        pass = new String(inputPassword.getPassword());
        if (correo.equals("")) {
            JOptionPane.showMessageDialog(null, "No puedes dejar el campo correo sin rellenar","Error", JOptionPane.WARNING_MESSAGE);
            correcto = false;
        }
        if (pass.equals("")) {
            JOptionPane.showMessageDialog(null, "No puedes dejar el campo contraseña sin rellenar","Error", JOptionPane.WARNING_MESSAGE);
            correcto = false;
        }
        // compruebo que la cuenta sea gmail o outlook
        if (!correo.contains("@gmail") && !correo.contains("@outlook")) {
            JOptionPane.showMessageDialog(null, "La cuenta de correo debe ser tipo GMAIL u OUTLOOK","Error", JOptionPane.WARNING_MESSAGE);
            correcto = false;
        }
        if (correcto) {
            if (modificar) {
                System.out.println("actulizando datos de email...");
                try {
                PreparedStatement insertarSQL;
                insertarSQL = this.bd.dameConexion().prepareStatement("UPDATE infoemail SET email = ?, password = ? WHERE id = ?");
                insertarSQL.setString(1, correo);
                insertarSQL.setString(2, pass);
                insertarSQL.setInt(3, 1);
                insertarSQL.executeUpdate();
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Datos del correo modificados correctamente","Exito al modificar", JOptionPane.INFORMATION_MESSAGE);
                aplicacion.muestraTablaOfertas("", "", false);
            } catch(SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage(),"Error SQL", JOptionPane.WARNING_MESSAGE);
            }
            } else {
                System.out.println("guardando datos de email...");
                try {
                    PreparedStatement insertarSQL;
                    insertarSQL = this.bd.dameConexion().prepareStatement("INSERT INTO infoemail (email,password) VALUES (?,?)");
                    insertarSQL.setString(1, correo);
                    insertarSQL.setString(2, pass);
                    insertarSQL.executeUpdate();
                    this.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Datos del correo guardados correctamente", "Exito al guardar", JOptionPane.INFORMATION_MESSAGE);
                    aplicacion.muestraTablaOfertas("", "", false);
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e.getMessage(), "Error SQL", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    
